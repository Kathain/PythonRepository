numbers = [4, 81, 96, 109, 87, 99, 61]


numbers.sort()              # данный метод сортирует наши числа по возрастанию
print(numbers)

numbers.sort(reverse=True)         # отсортирует по убыванию
print(numbers)

numbers[3] = 'B'                # заменяет элемент по индексу на нужный нам (как напримпер на букву)
print(numbers)

letters = ['a','b','v', 'c']
letters.sort()
print(letters)

